package ex05;

public class VarEx01 {
	public static void main(String[] args) {
		// 코드 스니펫: 문장을 완성시켜 주는 것
		
		int n1=10; // int = 42억 9천(경우의 수), 정수 최대 21개 표현(음수 포함) - 정수
		System.out.println(n1);
		
		double d1 = 10.5; // 실수 표현, 경개 표현, 정수 최대 21억(소수 포함) - 실수
		System.out.println(d1);
		
		long lo1 = 3000000000L; // L -> long 데이터 타입을 표시하기 위한 문법, 진짜 경까지 표현 - 정수
		System.out.println(lo1); // 출력시 보이지 않음
		
		boolean b1 = true; //1(true) or 0(false)
		System.out.println(b1);
	}
}
