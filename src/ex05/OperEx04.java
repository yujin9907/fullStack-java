package ex05;

public class OperEx04 {

	// 증가 감소, 단항연산자 90 페이지
	public static void main(String[] args) {
		int num =1;
		num = 2;
		System.out.println(num);
		
		// 증가식
		num = num+1;
		System.out.println(num);
		
		// 감소식
		int count = 1;
		count = count -1;
		System.out.println(count);
		
		int sum=0;
		sum+=10;
		System.out.println(sum);
	}

}
